<?php

namespace App\Models;

use Illuminate\Database\Eloquent\Model;

class ChatMessage extends Model
{
    protected $fillable = [
        'order_id',
        'sender_id',
        'message',
        'image',
    ];

    public function sender()
    {
        return $this->belongsTo(User::class, 'sender_id');
    }
}
